function mkpw($nchars)
{
	$allowed="0123456789ABCDEF";
	mt_srand ((double) microtime() * 100000000);
	while (strlen($return)<$nchars) $return .= $allowed[mt_rand(0,strlen($allowed))];
	return $return;
}
				
function makemac()
{
	for($i = 0; $i < 6; $i ++)
		$mac[] = mkpw(2);
	return join(':',$mac);
